/*
	1、萃取技巧，实际上是通过函数模板中的类型推导机制，以期在编译期获得函数的调用
	2、类型萃取就是为了解决和iterator有关的问题
	3、所谓template partial specialization和模板的默认参数是完全不同的两件事情，前者指的是
	当参数为一类特定类型时候，采用特殊设计，也就是说是“针对template参数更进一步的条件限制所
	设计出来的一个特化版本”；而默认参数则是当不提供某些参数的时候，使用的一个缺省
*/

